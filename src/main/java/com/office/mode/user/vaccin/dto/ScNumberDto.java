package com.office.mode.user.vaccin.dto;

public class ScNumberDto {

    private String sc_number;
    private String sc_name;
    private String sc_addr;
    private String sc_phone;
    private String sc_reg_date;
    private String sc_mod_date;

    public String getSc_number() {
        return sc_number;
    }

    public void setSc_number(String sc_number) {
        this.sc_number = sc_number;
    }

    public String getSc_name() {
        return sc_name;
    }

    public void setSc_name(String sc_name) {
        this.sc_name = sc_name;
    }

    public String getSc_addr() {
        return sc_addr;
    }

    public void setSc_addr(String sc_addr) {
        this.sc_addr = sc_addr;
    }

    public String getSc_phone() {
        return sc_phone;
    }

    public void setSc_phone(String sc_phone) {
        this.sc_phone = sc_phone;
    }

    public String getSc_reg_date() {
        return sc_reg_date;
    }

    public void setSc_reg_date(String sc_reg_date) {
        this.sc_reg_date = sc_reg_date;
    }

    public String getSc_mod_date() {
        return sc_mod_date;
    }

    public void setSc_mod_date(String sc_mod_date) {
        this.sc_mod_date = sc_mod_date;
    }
}
